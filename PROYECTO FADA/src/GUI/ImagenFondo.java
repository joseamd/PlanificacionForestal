
package GUI;
import java.awt.Component;
import java.awt.Graphics;
import java.awt.Insets;
import java.awt.image.BufferedImage;
import java.net.URL;
import javax.imageio.ImageIO;
import javax.swing.border.Border;

/**
 *
 * @author Alex
 */
public class ImagenFondo implements Border{//los bordes del JdesktopPane
    public BufferedImage back;//BufferedImage nos permite almacenar una imagen en memoria
 
    public ImagenFondo(){
        try {
            URL imagePath = new URL(getClass().getResource("/Im√°genes/LogoUnivalle.jpg").toString());
            back = ImageIO.read(imagePath);
        } catch (Exception ex) {            
        }
    }

    public void paintBorder(Component c, Graphics g, int x, int y, int width, int height) {
        g.drawImage(back, (x + (width - back.getWidth())/2),(y + (height - back.getHeight())/2), null);
    }
 
    public Insets getBorderInsets(Component c) {
        return new Insets(0,0,0,0);
    }
 
    public boolean isBorderOpaque() {
        return false;
    }
 
}

